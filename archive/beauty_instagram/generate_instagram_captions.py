# ── 品質チェック ─────────────────────────────────────────────────────────────

def validate_content(content: str) -> list:
    warnings = []

    # Day見出しの数
    day_headers = re.findall(r"--- Day\d+-\d+ ---", content)
    if len(day_headers) != 14:
        print(f"❌ [FATAL] Day見出しが {len(day_headers)} 個です（14個必要）")
        sys.exit(1)

    # 各セクションの存在チェック
    for label in ["画像案：", "キャプション：", "ハッシュタグ："]:
        count = content.count(label)
        if count != 14:
            warnings.append(f"「{label}」の数が {count} 個（期待: 14）")

    # 禁止表現チェック
    for word in FORBIDDEN_WORDS:
        if word in content:
            warnings.append(f"禁止表現が含まれています: 「{word}」")

    # ── note誘導チェック（キャプション本文のみ対象） ──────────────────────────
    # 「プロフィール」が明示されているものだけをnote誘導としてカウント
    # 「個人の体験です」「医師の指導のもと」などはカウントしない
    caption_blocks = re.findall(r"キャプション：(.+?)(?=ハッシュタグ：)", content, re.DOTALL)

    # 強い誘導: 「プロフィール」 + 具体的内容（費用・経過・後悔・使ったもの等）
    STRONG_KEYWORDS = ["費用", "経過", "後悔", "使ったもの", "クリニック", "まとめています", "すべて書いています"]
    # 弱い誘導: 「プロフィール」のみ（強い誘導に該当しないもの）

    strong_days = []
    weak_days = []
    url_in_caption = []

    for i, block in enumerate(caption_blocks, 1):
        cap = block.strip()
        day_label = f"Day{(i - 1) // 2 + 1}-{(i - 1) % 2 + 1}"

        # URL直貼りチェック
        if "http" in cap:
            url_in_caption.append(day_label)

        # note誘導判定（「プロフィール」が含まれている場合のみ）
        # 強い誘導: 具体的なコンテンツキーワードが2個以上含まれる
        if "プロフィール" in cap:
            matched = sum(1 for kw in STRONG_KEYWORDS if kw in cap)
            if matched >= 2:
                strong_days.append(day_label)
            else:
                weak_days.append(day_label)

    if url_in_caption:
        warnings.append(f"キャプション本文にURLが直貼りされています: {', '.join(url_in_caption)}")

    print(f"  強いnote誘導: {len(strong_days)}本 → {strong_days}")
    print(f"  弱いnote誘導: {len(weak_days)}本 → {weak_days}")

    if len(strong_days) < 2:
        warnings.append(f"強いnote誘導が{len(strong_days)}本です（目標: 2本）")
    if len(weak_days) < 4:
        warnings.append(f"弱いnote誘導が{len(weak_days)}本です（目標: 4本）")

    # ハッシュタグ過多チェック（ハッシュタグ欄のみ）
    hashtag_blocks = re.findall(r"ハッシュタグ：(.+?)(?=--- Day|\Z)", content, re.DOTALL)
    for i, block in enumerate(hashtag_blocks, 1):
        tag_count = len(re.findall(r"#\S+", block))
        if tag_count > 10:
            warnings.append(f"Day{i} ハッシュタグが多すぎます（{tag_count}個）")

    return warnings


# ── 生成・保存 ───────────────────────────────────────────────────────────────

def get_theme(week: int) -> str:
    cycle_pos = ((week - 1) % 4) + 1
    return WEEK_THEMES[cycle_pos]


def generate_captions(week: int, api_key: str) -> str:
    theme = get_theme(week)
    print(f"Week {week} / テーマ: {theme}")
    print(f"モデル: {MODEL_NAME}")
    print("Claude APIで生成中...")

    prompt = PROMPT_TEMPLATE.format(
        theme=theme,
        hashtags=HASHTAGS_BASE,
    )

    try:
        client = anthropic.Anthropic(api_key=api_key)
        message = client.messages.create(
            model=MODEL_NAME,
            max_tokens=8192,
            messages=[{"role": "user", "content": prompt}],
        )
        return message.content[0].text
    except Exception as e:
        print(f"⚠️ API生成失敗: {e}")
        print("fallbackコンテンツを使用します")
        return FALLBACK_CONTENT


def save_output(week: int, content: str) -> Path:
    theme = get_theme(week)
    out_dir = Path(__file__).parent / "instagram_captions"
    out_dir.mkdir(exist_ok=True)
    out_path = out_dir / f"captions_week{week}.txt"

    header = (
        f"Instagramキャプション Week {week}\n"
        f"テーマ: {theme}\n"
        f"note: {NOTE_URL}\n"
        f"\n"
        f"運用方針:\n"
        f"- 強いnote誘導: 2本\n"
        f"- 弱いnote誘導: 4本\n"
        f"- 信頼獲得投稿: 8本\n"
        f"- 投稿前に医療効果の断定表現を確認\n"
        f"- 画像案と実際の画像がズレる場合は手動修正\n"
        f"{'=' * 60}\n\n"
    )
    out_path.write_text(header + content, encoding="utf-8")
    return out_path


# ── メイン ───────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(description="Instagram キャプション生成")
    parser.add_argument("--week", type=int, required=True, help="週番号（1〜）")
    args = parser.parse_args()

    if args.week < 1:
        print("❌ --week は1以上の整数を指定してください")
        sys.exit(1)

    api_key = os.environ.get("ANTHROPIC_API_KEY", "")
    if not api_key:
        print("❌ ANTHROPIC_API_KEY が設定されていません")
        sys.exit(1)

    content = generate_captions(args.week, api_key)
    out_path = save_output(args.week, content)

    print(f"\n✅ 生成完了: {out_path}")
    print(f"   文字数: {len(content)}文字")

    print("\n--- 品質チェック ---")
    warnings = validate_content(content)
    if warnings:
        for w in warnings:
            print(f"⚠️  {w}")
    else:
        print("✅ 問題なし")


if __name__ == "__main__":
    main()
